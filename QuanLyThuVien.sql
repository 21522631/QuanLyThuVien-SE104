	NGAYTHU DATE NOT NULL,
	TONGNO INT,
	SOTIENTHU INT NOT NULL DEFAULT 0,
	CONLAI INT NOT NULL
	FOREIGN KEY (IDDOCGIA) REFERENCES DOCGIA(ID) ON DELETE CASCADE,
)

