(
	IDSACH INT,
	IDTACGIA INT,
	PRIMARY KEY (IDSACH, IDTACGIA),
	FOREIGN KEY (IDSACH)REFERENCES SACH(ID) ON DELETE CASCADE,
	FOREIGN KEY (IDTACGIA)REFERENCES TACGIA(ID) ON DELETE CASCADE,
