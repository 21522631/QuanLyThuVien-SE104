--TABLE NGUOIDUNG
GO
CREATE TABLE NGUOIDUNG
(
	ID INT IDENTITY PRIMARY KEY,
	MANGUOIDUNG AS CAST('ND'+ RIGHT('0000' + CAST(ID AS VARCHAR(4)), 4) AS CHAR(6)),
