--TABLE CUONSACH
GO
CREATE TABLE CUONSACH
(
	ID INT IDENTITY PRIMARY KEY,
	MACUONSACH AS CAST('CS'+ RIGHT('0000' + CAST(ID AS VARCHAR(4)), 4) AS CHAR(6)),
	IDSACH INT,
	TINHTRANG INT NOT NULL DEFAULT 1, --1 LA TRONG
