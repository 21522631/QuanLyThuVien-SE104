(
	IDPNS INT,
	IDSACH INT,
	SOLUONGNHAP INT NOT NULL, 
	DONGIA INT NOT NULL, 
	THANHTIEN INT NOT NULL, 
	PRIMARY KEY (IDPNS, IDSACH),
	FOREIGN KEY (IDPNS) REFERENCES PHIEUNHAPSACH(ID) ON DELETE CASCADE,
	FOREIGN KEY (IDSACH) REFERENCES SACH(ID) ON DELETE CASCADE,
