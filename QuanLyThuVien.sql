	FOREIGN KEY (IDBCTHMS) REFERENCES BC_TINHHINHMUONSACH(ID) ON DELETE CASCADE,
	FOREIGN KEY (IDTHELOAI) REFERENCES THELOAI(ID) ON DELETE CASCADE,
)

