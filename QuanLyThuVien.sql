--TABLE SACH
GO
CREATE TABLE SACH
(
	ID INT IDENTITY PRIMARY KEY,
	MASACH AS CAST('SA' + RIGHT('0000' + CAST(ID AS VARCHAR(4)), 4) AS CHAR(6)),
	TENSACH NVARCHAR(100) NOT NULL,
