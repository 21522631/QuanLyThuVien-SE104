(
	ID INT IDENTITY PRIMARY KEY,
	MALOAIDOCGIA  AS CAST('LDG'+ RIGHT('000' + CAST(ID AS VARCHAR(3)), 3) AS CHAR(6)),
	TENLOAIDOCGIA NVARCHAR(100) NOT NULL
)

--TABLE DOCGIA
GO
CREATE TABLE DOCGIA
(
	ID INT IDENTITY PRIMARY KEY,
	MADOCGIA AS CAST('DG' + RIGHT('0000' + CAST(ID AS VARCHAR(4)), 4) AS CHAR(6)),
	IDLOAIDOCGIA INT NOT NULL,
	TENDOCGIA NVARCHAR(100) NOT NULL,
	NGAYSINH DATE NOT NULL, 
	DIACHI NVARCHAR(100),
	EMAIL VARCHAR(100),
	NGAYLAPTHE DATE NOT NULL, 
	NGAYHETHAN DATE NOT NULL, 
	TONGNO INT NOT NULL DEFAULT 0,
	IDNGUOIDUNG INT NOT NULL,
	FOREIGN KEY (IDLOAIDOCGIA) REFERENCES LOAIDOCGIA(ID) ON DELETE CASCADE,
	FOREIGN KEY (IDNGUOIDUNG) REFERENCES NGUOIDUNG(ID) ON DELETE CASCADE,
)

--TABLE PHIEUMUONTRA
GO
CREATE TABLE PHIEUMUONTRA
(
	ID INT IDENTITY PRIMARY KEY,
	SOPMT AS CAST('PMT' + RIGHT('0000' + CAST(ID AS VARCHAR(4)), 4) AS CHAR(7)),
	IDDOCGIA INT NOT NULL,
	IDCUONSACH INT NOT NULL,
	NGAYMUON DATE NOT NULL, 
	NGAYPHAITRA DATE NOT NULL, 
	NGAYTRA DATE,
	TIENPHAT INT DEFAULT 0,
	FOREIGN KEY (IDDOCGIA) REFERENCES DOCGIA(ID) ON DELETE CASCADE,
	FOREIGN KEY (IDCUONSACH) REFERENCES CUONSACH(ID) ON DELETE CASCADE,
)

--TABLE PHIEUTHUTIENPHAT
GO
